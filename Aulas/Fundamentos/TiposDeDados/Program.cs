Console.WriteLine("Tipos de Dados");

Console.WriteLine("Tipos númericos (sem ponto flutuante):");

byte valor1 = 10;
short valor2 = 20;
int valor3 = 30;
long valor4 = 40L;

Console.WriteLine(valor1 + valor2 + valor3 + valor4);

Console.WriteLine("Tipos númericos (com ponto flutuante):");

float valor5 = 10.0F;
double valor6 = 20.0D;
decimal valor7 = 30.0M;

Console.WriteLine(valor5);
Console.WriteLine(valor6);
Console.WriteLine(valor7);

Console.WriteLine("Tipo carácter:");

char valor8 = 'A';

Console.WriteLine(valor8);

Console.WriteLine("Tipo booleano:");

bool valor9 = true; // false

Console.WriteLine(valor9);

Console.WriteLine("Tipo texto:"); // Tipo referência

String valor10 = "Arthur";

Console.WriteLine(valor10);